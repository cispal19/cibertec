    public void cargarCombo() {
        cbxCurso.removeAllItems();
        cbxCurso.addItem("Seleccione");
        CursoService cursoService = ctx.getBean(CursoService.class);
        List<Curso> listaCurso = cursoService.listarAll();
        for (Curso col : listaCurso) {
            cbxCurso.addItem(col);

        }

    }
