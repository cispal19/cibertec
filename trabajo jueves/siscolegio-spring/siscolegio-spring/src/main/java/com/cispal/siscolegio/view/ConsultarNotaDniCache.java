    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        NotaService notaService = ctx.getBean(NotaService.class);
        Alumno alumno = new Alumno();
        alumno.setDni(txtDni.getText());
        nota.setAlumno(alumno);
        nota.setUnidad(cbxBimestre.getSelectedItem().toString());
        nota.setCurso((Curso) cbxCurso.getSelectedItem());

        Notas notaObtenida = notaService.consultarNotaByDniCursoBimestre(nota);
        if (notaObtenida != null) {
            txtApellidos.setText(notaObtenida.getAlumno().getApellidos());
            txtNombres.setText(notaObtenida.getAlumno().getNombres());
            txtNota.setText(""+notaObtenida.getPromedio());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

